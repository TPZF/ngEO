# ----------------------------------------------------------------------------
# Configuration section
# ----------------------------------------------------------------------------
# 
INTERFACE="eth0"
# Broadcast address
BCASTADDR="192.168.151.255"

# ----------------------------------------------------------------------------
# End of configuration section
# ----------------------------------------------------------------------------

# ----------------------------------------------------------------------------
# Start
# ----------------------------------------------------------------------------
ngeo_start() {  
    echo "------------------------------------------------------------------------------"
    echo " NGEO Service Start"
    echo "------------------------------------------------------------------------------"
	service httpd start
}

# ----------------------------------------------------------------------------
# Start
# ----------------------------------------------------------------------------
ngeo_stop() {
    echo "------------------------------------------------------------------------------"
    echo " NGEO Service Stop"
    echo "------------------------------------------------------------------------------"
	service httpd stop

}

# ----------------------------------------------------------------------------
# Start
# ----------------------------------------------------------------------------
ngeo_status() {
    echo -n "Checking for service ${MYSELF}:"
	service httpd status
}

# ----------------------------------------------------------------------------
# Main
# ----------------------------------------------------------------------------
case "$1" in
start)
    ngeo_start
    ngeo_status
;;
stop)
    ngeo_stop
;;
restart)
    ngeo_stop
    ngeo_start
    ngeo_status
;;
status)
    ngeo_status
;;
*)
    echo "Usage: $0 {start|stop|restart|status}"
exit 1
;;
esac

